package basic_05_23Dec;

public class Lab066 {

	public static void main(String[] args) {
		
		//  // Print the Odd number in between the 1 to 50.
        // If Else
		
		for(int i=1; i<=50; i++) 
		{
			if(i%2==0) 
			{
			System.out.println(i +" is Even number");
			}
			else 
			{
				System.out.println(i +" is Odd number");
			}
		
		}

	}

}
